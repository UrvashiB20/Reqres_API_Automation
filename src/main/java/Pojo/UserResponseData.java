package Pojo;

public class UserResponseData {

    private String name;
    private String job;
    private String id;
    private String createdAt;
    private String updatedAt;

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }

    public String getId() {
        return id;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public String getCreatedAt() {
        return createdAt;
    }
}
